      currentSlide = 0;
    }
    slideImg.animate({"margin-left": -currentSlide * slideWidth + "px"},500);
  }

 
  slideBtn.on("click", ".slide_btn", function() {
    if ($(this).hasClass("prev")) {
      back();
    } else {
      next();
    }
